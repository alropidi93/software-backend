<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class MovimientoTipoStock extends Model
{
    protected $table = 'movimientoTipoStock';
    public $timestamps = true;
  
    protected $fillable = [
      'id',
      'idProducto',
      'idAlmacen',
      'idTipoStock',
      'idUsuario',
      'cantidad',
      'signo',
      'deleted'
    ];

    public function producto(){
        return $this->hasOne('App\Models\Producto', 'idProducto', 'id');
    }
    
    public function almacen(){
        return $this->hasOne('App\Models\Almacen', 'idAlmacen', 'id');
    }

    public function tipoStock(){
        return $this->hasOne('App\Models\TipoStock', 'idTipoStock', 'id');
    }

    public function usuario(){
        return $this->hasOne('App\Models\Usuario', 'idUsuario', 'idPersonaNatural');
    }
}

